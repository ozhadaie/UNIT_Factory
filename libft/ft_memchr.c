#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	size_t			i;

	str = (unsigned char*)s;
	i = -1;
	while (++i < n)
		if (str[i] == (unsigned char)c)
			return ((void*)(str + i));
	return (NULL);
}
